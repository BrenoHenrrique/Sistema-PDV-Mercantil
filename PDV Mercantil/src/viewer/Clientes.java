    private void botExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botExcluirActionPerformed
        int linha = tabelaCliente.getSelectedRow();
        String codigo = tabelaCliente.getValueAt(linha, 0).toString();

        int resposta = JOptionPane.showConfirmDialog(null, "Deseja excluir este cliente?");
        if (resposta == JOptionPane.YES_OPTION) {
            conexao.conecta();
            try {
                stmt = conexao.conn.prepareStatement("delete from cliente where idCliente='" + codigo + "'");
                stmt.execute();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
        conexao.desconecta();
        readTableCliente("select * from cliente order by nome");
    }//GEN-LAST:event_botExcluirActionPerformed

